import numpy as np

# create two vectors
vector_a = np.array([1, 2])
vector_b = np.array([3, 4])

# np.dot() multiplies vectors

dot_product = np.dot(vector_a, vector_b)

# print vectors
print("vector a:", vector_a)
print("vector b:", vector_b)

# print result
print("dot product:", dot_product)
